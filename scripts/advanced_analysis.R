# Returns a complete named colour vector even when values are not in the palette
safe_pal <- function(pal, vals) {
  vals    <- as.character(vals)
  present <- intersect(vals, names(pal))
  missing <- setdiff(vals, names(pal))
  extra   <- setNames(grey.colors(max(length(missing), 1L), start = 0.5), missing)
  c(pal[present], extra)[vals]
}

