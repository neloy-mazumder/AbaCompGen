# ── 1b. SAFE FILE LOADER ──────────────────────────────────────────────────────
# Loads a table with a warning instead of crashing if the file is missing.
safe_load <- function(path, reader = read_tsv, ...) {
  if (!file.exists(path)) {
    warning(sprintf("[SKIP] File not found: %s", path), call. = FALSE)
    return(NULL)
  }
  tryCatch(
    reader(path, show_col_types = FALSE, ...),
    error = function(e) {
      warning(sprintf("[ERROR] Could not read %s: %s", path, e$message), call. = FALSE)
      NULL
    }
  )
}

# ── 2. PUBLICATION THEME ───────────────────────────────────────────────────────
theme_pub <- function(base_size = 12) {
  theme_classic(base_size = base_size) +
    theme(
      axis.line         = element_line(colour = "black", linewidth = 0.5),
      axis.ticks        = element_line(colour = "black", linewidth = 0.4),
      axis.text         = element_text(colour = "black", size = base_size - 1),
      axis.title        = element_text(colour = "black", size = base_size, face = "bold"),
      panel.grid        = element_blank(),
      panel.background  = element_blank(),
      plot.background   = element_blank(),
      legend.background = element_blank(),
      legend.key        = element_blank(),
      legend.title      = element_text(size = base_size - 1, face = "bold"),
      legend.text       = element_text(size = base_size - 2),
      plot.title        = element_text(size = base_size + 2, face = "bold",
                                       hjust = 0, margin = margin(b = 4)),
      plot.subtitle     = element_text(size = base_size - 1, colour = "grey40",
                                       hjust = 0, margin = margin(b = 8)),
      plot.caption      = element_text(size = base_size - 3, colour = "grey55", hjust = 1),
      strip.background  = element_rect(fill = "grey93", colour = NA),
      strip.text        = element_text(size = base_size - 1, face = "bold"),
      plot.margin       = margin(10, 14, 8, 10)
    )
}

